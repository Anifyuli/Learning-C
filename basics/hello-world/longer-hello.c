#include <stdio.h>

int main(){
    // \n\n\ is operator to make a new line

    printf("Hello World!\n\n");

    /* 
    \t used to add a horizontal tab
    \" to add double quote
    \\ to add backslash
    */
    printf("I am learning C. \n\n \t I enjoy it. \"I love\\like C!\" ");
    return 0;
}